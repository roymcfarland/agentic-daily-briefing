import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const root = process.cwd();
const schemaPath = resolve(root, "openapi/blueprint.openapi.json");
const outputPath = resolve(root, "lib/blueprint/generated/client.ts");

const schemaRaw = await readFile(schemaPath, "utf8");
const schema = JSON.parse(schemaRaw);
const taskSchema = schema.components.schemas.Task;
const dailySummaryEntry = Object.entries(schema.paths).find(
  ([, pathItem]) => pathItem?.get?.operationId === "getDailySummary",
);

if (!dailySummaryEntry) {
  throw new Error("Blueprint OpenAPI spec is missing getDailySummary.");
}

const [dailySummaryPath, dailySummaryPathItem] = dailySummaryEntry;
const dailySummarySchema =
  dailySummaryPathItem.get.responses["200"].content["application/json"].schema;

const taskStatusValues = taskSchema.properties.status.enum
  .map((value) => `"${value}"`)
  .join(" | ");
const taskCategoryEnum = taskSchema.properties.category.enum;
const taskCategoryValues = taskCategoryEnum
  .map((value) => `"${value}"`)
  .join(" | ");

function kebabToCamel(value) {
  return value.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

const byCategoryFields = taskCategoryEnum
  .map((value) => `      ${kebabToCamel(value)}?: number;`)
  .join("\n");

const isTaskCategoryChecks = taskCategoryEnum
  .map((value, index) => {
    const prefix = index === 0 ? "    " : "    || ";
    return `${prefix}value === "${value}"`;
  })
  .join("\n");

const file = `/* eslint-disable */
/* Auto-generated by scripts/generate-blueprint-client.mjs from openapi/blueprint.openapi.json. */

export type Task = {
  id?: number;
  title?: string;
  description?: string | null;
  status?: ${taskStatusValues};
  category?: ${taskCategoryValues};
  parentId?: number | null;
  sortOrder?: number;
  createdAt?: string;
  updatedAt?: string;
};

export type DailySummaryResponse = {
  generatedAt?: string;
  summary?: {
    totalActive?: number;
    completionRate?: string;
    byStatus?: {
      iceBox?: number;
      onDeck?: number;
      inProgress?: number;
      done?: number;
      archived?: number;
    };
    byCategory?: {
${byCategoryFields}
    };
  };
  inProgress?: Task[];
  onDeck?: Task[];
  iceBox?: Task[];
  recentlyCompleted?: Task[];
};

export interface BlueprintClientConfig {
  baseUrl: string;
  apiKey: string;
  timeoutMs?: number;
}

function isObject(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null;
}

function optionalString(value: unknown): string | undefined {
  return typeof value === "string" ? value : undefined;
}

function optionalNullableString(value: unknown): string | null | undefined {
  return value === null ? null : optionalString(value);
}

function optionalNumber(value: unknown): number | undefined {
  return typeof value === "number" && Number.isFinite(value) ? value : undefined;
}

function isTaskStatus(value: unknown): value is Task["status"] {
  return (
    value === "ice-box" ||
    value === "on-deck" ||
    value === "in-progress" ||
    value === "done" ||
    value === "archived"
  );
}

function isTaskCategory(value: unknown): value is Task["category"] {
  return (
${isTaskCategoryChecks}
  );
}

function parseTask(value: unknown): Task | null {
  if (!isObject(value)) {
    return null;
  }

  if (value.id != null && optionalNumber(value.id) === undefined) {
    return null;
  }

  if (value.parentId != null && optionalNumber(value.parentId) === undefined) {
    return null;
  }

  if (value.sortOrder != null && optionalNumber(value.sortOrder) === undefined) {
    return null;
  }

  if (value.status != null && !isTaskStatus(value.status)) {
    return null;
  }

  if (value.category != null && !isTaskCategory(value.category)) {
    return null;
  }

  return {
    id: optionalNumber(value.id),
    title: optionalString(value.title),
    description: optionalNullableString(value.description),
    status: isTaskStatus(value.status) ? value.status : undefined,
    category: isTaskCategory(value.category) ? value.category : undefined,
    parentId: value.parentId === null ? null : optionalNumber(value.parentId),
    sortOrder: optionalNumber(value.sortOrder),
    createdAt: optionalString(value.createdAt),
    updatedAt: optionalString(value.updatedAt),
  };
}

function toTaskArray(value: unknown): Task[] {
  if (!Array.isArray(value)) {
    return [];
  }

  return value.flatMap((entry) => {
    const task = parseTask(entry);
    return task ? [task] : [];
  });
}

function isAbortError(error: unknown): boolean {
  return error instanceof Error && error.name === "AbortError";
}

function parseDailySummaryResponse(value: unknown): DailySummaryResponse {
  if (!isObject(value)) {
    throw new Error("Blueprint getDailySummary returned an invalid response body");
  }

  return {
    generatedAt: typeof value.generatedAt === "string" ? value.generatedAt : undefined,
    summary: isObject(value.summary)
      ? {
          totalActive:
            typeof value.summary.totalActive === "number" ? value.summary.totalActive : undefined,
          completionRate:
            typeof value.summary.completionRate === "string"
              ? value.summary.completionRate
              : undefined,
          byStatus: isObject(value.summary.byStatus) ? value.summary.byStatus : undefined,
          byCategory: isObject(value.summary.byCategory) ? value.summary.byCategory : undefined,
        }
      : undefined,
    inProgress: toTaskArray(value.inProgress),
    onDeck: toTaskArray(value.onDeck),
    iceBox: toTaskArray(value.iceBox),
    recentlyCompleted: toTaskArray(value.recentlyCompleted),
  };
}

export class BlueprintClient {
  constructor(private readonly config: BlueprintClientConfig) {}

  async getDailySummary(): Promise<DailySummaryResponse> {
    const timeoutMs = this.config.timeoutMs ?? 12000;
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), timeoutMs);

    try {
      const response = await fetch(\`\${this.config.baseUrl.replace(/\\/$/, "")}${dailySummaryPath}\`, {
        method: "GET",
        headers: {
          accept: "application/json",
          authorization: \`Bearer \${this.config.apiKey}\`,
        },
        signal: controller.signal,
      });

      if (!response.ok) {
        throw new Error(\`Blueprint getDailySummary failed with \${response.status}\`);
      }

      const contentType = response.headers.get("content-type") ?? "";
      if (!contentType.toLowerCase().includes("application/json")) {
        throw new Error("Blueprint getDailySummary returned a non-JSON response");
      }

      let body: unknown;
      try {
        body = await response.json();
      } catch {
        throw new Error("Blueprint getDailySummary returned malformed JSON");
      }

      return parseDailySummaryResponse(body);
    } catch (error) {
      if (isAbortError(error)) {
        throw new Error(\`Blueprint getDailySummary timed out after \${timeoutMs}ms\`);
      }

      throw error;
    } finally {
      clearTimeout(timeout);
    }
  }
}
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, file, "utf8");

const log = {
  generated: outputPath,
  responseFields: Object.keys(dailySummarySchema.properties),
  taskFields: Object.keys(taskSchema.properties),
};

console.log(JSON.stringify(log, null, 2));
