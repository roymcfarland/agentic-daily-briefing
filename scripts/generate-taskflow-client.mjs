import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const root = process.cwd();
const schemaPath = resolve(root, "openapi/taskflow.openapi.json");
const outputPath = resolve(root, "lib/taskflow/generated/client.ts");

const schemaRaw = await readFile(schemaPath, "utf8");
const schema = JSON.parse(schemaRaw);
const requestSchema = schema.components.schemas.DailySummaryRequest;
const responseSchema = schema.components.schemas.DailySummaryResponse;
const areaValues = requestSchema.properties.area.enum.map((value) => `"${value}"`).join(" | ");

const file = `/* eslint-disable */
/* Auto-generated by scripts/generate-taskflow-client.mjs from openapi/taskflow.openapi.json. */

export type DailySummaryRequest = {
  area: ${areaValues};
  asOfDate?: string;
};

export type DailySummaryResponse = {
  area: string;
  headline: string;
  openItems: number;
  blockers: string[];
  priorities: string[];
  dueToday: string[];
  rawSummary?: string;
};

export interface TaskflowClientConfig {
  baseUrl: string;
  apiKey: string;
  timeoutMs?: number;
}

export class TaskflowClient {
  constructor(private readonly config: TaskflowClientConfig) {}

  async getDailySummary(body: DailySummaryRequest): Promise<DailySummaryResponse> {
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), this.config.timeoutMs ?? 12000);

    try {
      const response = await fetch(\`\${this.config.baseUrl.replace(/\\/$/, "")}/daily-summary\`, {
        method: "POST",
        headers: {
          "content-type": "application/json",
          authorization: \`Bearer \${this.config.apiKey}\`,
        },
        body: JSON.stringify(body),
        signal: controller.signal,
      });

      if (!response.ok) {
        throw new Error(\`Taskflow getDailySummary failed with \${response.status}\`);
      }

      return (await response.json()) as DailySummaryResponse;
    } finally {
      clearTimeout(timeout);
    }
  }
}
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, file, "utf8");

const log = {
  generated: outputPath,
  requestFields: Object.keys(requestSchema.properties),
  responseFields: Object.keys(responseSchema.properties),
};

console.log(JSON.stringify(log, null, 2));
