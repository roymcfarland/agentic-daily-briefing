import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const root = process.cwd();
const schemaPath = resolve(root, "openapi/taskflow.openapi.json");
const outputPath = resolve(root, "lib/taskflow/generated/client.ts");

const schemaRaw = await readFile(schemaPath, "utf8");
const schema = JSON.parse(schemaRaw);
const taskSchema = schema.components.schemas.Task;
const dailySummarySchema =
  schema.paths["/api/external/daily-summary"].get.responses["200"].content["application/json"]
    .schema;

const taskStatusValues = taskSchema.properties.status.enum
  .map((value) => `"${value}"`)
  .join(" | ");
const taskCategoryValues = taskSchema.properties.category.enum
  .map((value) => `"${value}"`)
  .join(" | ");

const file = `/* eslint-disable */
/* Auto-generated by scripts/generate-taskflow-client.mjs from openapi/taskflow.openapi.json. */

export type Task = {
  id?: number;
  title?: string;
  description?: string | null;
  status?: ${taskStatusValues};
  category?: ${taskCategoryValues};
  parentId?: number | null;
  sortOrder?: number;
  createdAt?: string;
  updatedAt?: string;
};

export type DailySummaryResponse = {
  generatedAt?: string;
  summary?: {
    totalActive?: number;
    completionRate?: string;
    byStatus?: {
      iceBox?: number;
      onDeck?: number;
      inProgress?: number;
      done?: number;
      archived?: number;
    };
    byCategory?: {
      personal?: number;
      elevatedOrganics?: number;
      brightlineLabs?: number;
    };
  };
  inProgress?: Task[];
  onDeck?: Task[];
  iceBox?: Task[];
  recentlyCompleted?: Task[];
};

export interface TaskflowClientConfig {
  baseUrl: string;
  apiKey: string;
  timeoutMs?: number;
}

function isObject(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null;
}

function isTask(value: unknown): value is Task {
  if (!isObject(value)) {
    return false;
  }

  if (value.id != null && typeof value.id !== "number") {
    return false;
  }

  if (value.parentId != null && typeof value.parentId !== "number") {
    return false;
  }

  return true;
}

function toTaskArray(value: unknown): Task[] {
  if (!Array.isArray(value)) {
    return [];
  }

  return value.filter(isTask);
}

function parseDailySummaryResponse(value: unknown): DailySummaryResponse {
  if (!isObject(value)) {
    throw new Error("Taskflow getDailySummary returned an invalid response body");
  }

  return {
    generatedAt: typeof value.generatedAt === "string" ? value.generatedAt : undefined,
    summary: isObject(value.summary)
      ? {
          totalActive:
            typeof value.summary.totalActive === "number" ? value.summary.totalActive : undefined,
          completionRate:
            typeof value.summary.completionRate === "string"
              ? value.summary.completionRate
              : undefined,
          byStatus: isObject(value.summary.byStatus) ? value.summary.byStatus : undefined,
          byCategory: isObject(value.summary.byCategory) ? value.summary.byCategory : undefined,
        }
      : undefined,
    inProgress: toTaskArray(value.inProgress),
    onDeck: toTaskArray(value.onDeck),
    iceBox: toTaskArray(value.iceBox),
    recentlyCompleted: toTaskArray(value.recentlyCompleted),
  };
}

export class TaskflowClient {
  constructor(private readonly config: TaskflowClientConfig) {}

  async getDailySummary(): Promise<DailySummaryResponse> {
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), this.config.timeoutMs ?? 12000);

    try {
      const response = await fetch(\`\${this.config.baseUrl.replace(/\\/$/, "")}/api/external/daily-summary\`, {
        method: "GET",
        headers: {
          authorization: \`Bearer \${this.config.apiKey}\`,
        },
        signal: controller.signal,
      });

      if (!response.ok) {
        throw new Error(\`Taskflow getDailySummary failed with \${response.status}\`);
      }

      const contentType = response.headers.get("content-type") ?? "";
      if (!contentType.toLowerCase().includes("application/json")) {
        throw new Error("Taskflow getDailySummary returned a non-JSON response");
      }

      return parseDailySummaryResponse(await response.json());
    } finally {
      clearTimeout(timeout);
    }
  }
}
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, file, "utf8");

const log = {
  generated: outputPath,
  responseFields: Object.keys(dailySummarySchema.properties),
  taskFields: Object.keys(taskSchema.properties),
};

console.log(JSON.stringify(log, null, 2));
